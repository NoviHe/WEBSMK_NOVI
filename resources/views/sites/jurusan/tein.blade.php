                            <h4>Kompetensi Keahlian Teknik Elektronika Industri</h4>
                            <p><a href="tein.html">Kompetensi Keahlian Teknik Elektronika Industri (TEIN)</a> merupakan salah satu kompetensi keahlian unggulan di SMK Negeri 1 Padaherang pada rumpun teknologi. Kompetensi keahlian Teknik Elektronika Industri (TEIN) terus mengalami kemajuan dan peningkatan dalam prestasi dan minat peserta didik. Pada tahun ajaran baru 2016/2017, SMK Negeri 1 Padaherang menerima siswa baru untuk kompetensi keahlian Teknik Elektronika Industri (TEIN) sebanyak 3 rombel dari 18 rombel yang tersedia.</p>

                            <p>Kompetensi Keahlian Teknik Elektronika Industri (TEIN) mendidik siswa agar mempunyai kemampuan / keterampilan kepada anak didik pada bidang sistem kontrol dan maintenance peralatan industri berbasis electrical control dan micro processor. Kompetensi tersebut meliputi pengetahuan dan keterampilan elektronika umum, mikrokontroller dan mikroprocessor, pneumatic dan PLC, programming berbasis komputer yang erat kaitannya dengan proses produksi di Industri.</p>

                            <p>Peserta didik Kompetensi Keahlian Teknik Elektronika Industri (TEIN) SMKN 1 Padaherang mampu membuat aplikasi-aplikasi seperti : merancang dan membuat lampu garasi terprogram, kontrol tower air yang tepat guna, alarm pengaman rumah dan berbagai macam aplikasi pemakaian sensor-sensor.</p>

                            <p>Untuk dapat mencapai lulusan yang berkualitas dan memenuhi target dunia usaha (industri), maka selama tiga tahun para siswa disiapkan fasilitas sebagai berikut : Laboratorium Elektronika Dasar, Laboratorium Elektronika Digital, Laboratorium Microprocessor dan Microcontrollerdan Laboratorim Komputer.</p>
                            
